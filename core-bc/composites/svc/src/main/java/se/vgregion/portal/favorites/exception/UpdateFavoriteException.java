package se.vgregion.portal.favorites.exception;


public class UpdateFavoriteException extends Exception {

	public UpdateFavoriteException() {
		super();
	}

	public UpdateFavoriteException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public UpdateFavoriteException(String arg0) {
		super(arg0);
	}

	public UpdateFavoriteException(Throwable arg0) {
		super(arg0);
	}

	
	
}
